/* eslint-env node */

/**
 * Generate logo SVG data as TypeScript string constants.
 * Reads SVGs from src/components/modus-wc-logo/assets/logos/
 * and outputs src/components/modus-wc-logo/logo-svg-data.ts
 *
 * Usage: node scripts/generate-logo-data.js
 * Runs automatically as part of the prebuild step.
 */

import { readFileSync, writeFileSync, readdirSync, statSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ASSETS_DIR = join(
  __dirname,
  '../src/components/modus-wc-logo/assets/logos'
);
const OUTPUT_PATH = join(
  __dirname,
  '../src/components/modus-wc-logo/logo-svg-data.ts'
);

function walkSvgs(dir, prefix = '') {
  const result = [];
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    const rel = prefix ? `${prefix}/${entry}` : entry;
    if (statSync(full).isDirectory()) {
      result.push(...walkSvgs(full, rel));
    } else if (entry.endsWith('.svg')) {
      result.push({ rel, full });
    }
  }
  return result;
}

const files = walkSvgs(ASSETS_DIR);
const lines = [
  '// Auto-generated by scripts/generate-logo-data.js',
  '// DO NOT EDIT MANUALLY',
  '',
  'export const LOGO_SVGS: Record<string, string> = {',
];

for (const { rel, full } of files.sort((a, b) => a.rel.localeCompare(b.rel))) {
  const svg = readFileSync(full, 'utf8')
    .replace(/\n/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
  const escaped = svg
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$/g, '\\$');
  lines.push(`  'logos/${rel}': \`${escaped}\`,`);
}

lines.push('};');
lines.push('');

writeFileSync(OUTPUT_PATH, lines.join('\n'), 'utf8');
globalThis.console.log(
  `Generated ${files.length} logo SVG entries in logo-svg-data.ts`
);
